package tp5.minesweeper.version1;

import java.awt.Color;

public class Cell {
	boolean isRevealed;
	boolean isMine;
	Color color = Color.GRAY;
	
	public Cell() {
		
	}
	
	public void setRevealed(boolean status) {
		this.isRevealed = status;
		
		if(status && !this.isMine)
			this.color = Color.DARK_GRAY;
		
			
	}
	

}
